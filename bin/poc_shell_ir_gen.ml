(* RFC-0054 POC — codegen-based walker generator.

   Bypasses the ppxlib AST-vs-source divergence (PR-1 + PR-1b empirical
   findings, RFC-0054 §5.3.1 + §5.3.3) by generating real OCaml source
   that the standard parser handles. No ppxlib involvement.

   Hardcoded synthetic GADT spec — same shape as the failed
   test_typed_param.ml under PPX. If this POC works, the same approach
   scales to lib/exec/shell_ir_typed.ml (RFC-0054 PR-2/3/4).

   Usage:
     poc_shell_ir_gen > synthetic_walkers.ml
   Or via dune (rule ...). *)

(* ─── Spec: per-constructor metadata ─────────────────────────────── *)

type ctor_spec =
  { name : string  (* OCaml constructor name, e.g. "C_ls" *)
  ; pattern : string  (* match pattern, e.g. "C_ls _" or "C_rm _" *)
  ; risk : string  (* polymorphic-variant value, e.g. "`Safe" *)
  ; sandbox : string  (* polymorphic-variant value, e.g. "`Host" *)
  }

let synthetic_command_spec : ctor_spec list =
  [ { name = "C_ls"
    ; pattern = "C_ls _"
    ; risk = "`Safe"
    ; sandbox = "`Host"
    }
  ; { name = "C_git_status"
    ; pattern = "C_git_status _"
    ; risk = "`Audited"
    ; sandbox = "`Host"
    }
  ; { name = "C_rm"
    ; pattern = "C_rm _"
    ; risk = "`Privileged"
    ; sandbox = "`Host"
    }
  ]

(* ─── Generator: emit OCaml source ───────────────────────────────── *)

let emit_header buf =
  Buffer.add_string buf
    "(* RFC-0054 POC — auto-generated by bin/poc_shell_ir_gen.\n\
    \   DO NOT EDIT.  Regenerated from the spec on every build.\n\
    \   The spec lives in bin/poc_shell_ir_gen.ml; the dune rule\n\
    \   re-emits this file when the generator changes. *)\n\n"

let emit_risk buf spec =
  Buffer.add_string buf
    "let risk\n\
    \  : type i o r s. (i, o, r, s) Synthetic_command.command\n\
    \    -> [ `Safe | `Audited | `Privileged ]\n\
    \  = function\n";
  List.iter
    (fun s ->
      Buffer.add_string buf
        (Printf.sprintf "  | Synthetic_command.%s -> %s\n" s.pattern s.risk))
    spec;
  Buffer.add_string buf "\n"

let emit_sandbox buf spec =
  Buffer.add_string buf
    "let sandbox\n\
    \  : type i o r s. (i, o, r, s) Synthetic_command.command\n\
    \    -> [ `Host | `Docker ]\n\
    \  = function\n";
  List.iter
    (fun s ->
      Buffer.add_string buf
        (Printf.sprintf "  | Synthetic_command.%s -> %s\n" s.pattern s.sandbox))
    spec;
  Buffer.add_string buf "\n"

let emit_all_constructors buf spec =
  Buffer.add_string buf "let all_constructor_names : string list =\n  [ ";
  let names = List.map (fun s -> Printf.sprintf "%S" s.name) spec in
  Buffer.add_string buf (String.concat "\n  ; " names);
  Buffer.add_string buf "\n  ]\n\n"

let () =
  let buf = Buffer.create 1024 in
  emit_header buf;
  emit_risk buf synthetic_command_spec;
  emit_sandbox buf synthetic_command_spec;
  emit_all_constructors buf synthetic_command_spec;
  print_string (Buffer.contents buf)
